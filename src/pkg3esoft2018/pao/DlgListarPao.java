    public DlgListarPao(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        
        this.myTableModel = new AbstractTableModel() {
            @Override
            public int getRowCount() {
                return paes.size();
            }

            @Override
            public int getColumnCount() {
                return 2;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                switch (columnIndex) {
                    case 0: return paes.get(rowIndex).getId();
                    case 1: return paes.get(rowIndex).getNome();
                }
                return "XXX";
            }

            @Override
            public String getColumnName(int column) {
                String[] colunas = {"ID", "Nome"};
                try {
                    return colunas[column];
                } catch (ArrayIndexOutOfBoundsException e) {
                    return "xxx";
                }
            }

        };
        initComponents();
    }

