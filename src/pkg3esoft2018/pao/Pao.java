package pkg3esoft2018.pao;

public class Pao {
    private Integer id;
    private String nome;

    public Pao(Integer id, String nome) {
        this.id = id;
        this.nome = nome;
    }
    
    public Integer getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }
    
    
    
}
